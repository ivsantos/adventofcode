const openingChars = ['(', '[', '{', '<'];
const closingChars = [')', ']', '}', '>'];

const illegalChars = [];

for (let i = 0; i < input.length; i++) {
  const pile = [];
  for (let j = 0; j < input[i].length; j++) {
    const char = input[i][j];
    if (openingChars.includes(char)) {
      pile.push(char);
    } else if (
      closingChars.includes(char) &&
      closingChars.indexOf(char) !== openingChars.indexOf(pile.pop())
    ) {
      illegalChars.push(char);
    }
  }
}

// Mapped to 'closingChars' array.
const points = [3, 57, 1197, 25137];
const sum = illegalChars.reduce((acc, curr) => acc + points[closingChars.indexOf(curr)], 0);
