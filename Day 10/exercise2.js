for (let i = 0; i < input.length; i++) {
  input[i] = input[i].split('');
}

const openingChars = ['(', '[', '{', '<'];
const closingChars = [')', ']', '}', '>'];

const incompleteLines = [];

for (let i = 0; i < input.length; i++) {
  let pile = [];
  for (let j = 0; j < input[i].length; j++) {
    const char = input[i][j];
    if (openingChars.includes(char)) {
      pile.push(char);
    } else if (
      closingChars.includes(char) &&
      closingChars.indexOf(char) !== openingChars.indexOf(pile.slice(-1)[0])
    ) {
      pile = [];
      break;
    } else {
      pile.pop();
    }
  }
  if (pile.length) {
    incompleteLines.push(pile);
  }
}

// Mapped to 'closingChars' array.
const points = [1, 2, 3, 4];
const score = [];
for (let i = 0; i < incompleteLines.length; i++) {
  const missingChars = [];
  for (let j = incompleteLines[i].length - 1; j >= 0; j--) {
    missingChars.push(closingChars[openingChars.indexOf(incompleteLines[i][j])]);
  }
  score.push(missingChars.reduce((acc, curr) => 5 * acc + points[closingChars.indexOf(curr)], 0));
}

// Ascending sort.
score.sort((a, b) => a - b);
const result = score[Math.floor(score.length / 2)];
console.log(`Result is ${result}`);
